// src/lib/analytics.ts
export const pageview = (url: string) => {
  window.gtag('config', 'G-14S5FZB356', {
    page_path: url,
  });
};

export const event = ({
  action,
  category,
  label,
  value,
}: {
  action: string;
  category: string;
  label: string;
  value?: number;
}) => {
  window.gtag('event', action, {
    event_category: category,
    event_label: label,
    value: value,
  });
};